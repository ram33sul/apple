function drop_down(){
    document.getElementById("drop-down").style.height='100vh';
    document.getElementById("nav-cross-logo").style.display='block';
    document.getElementById("nav-logo").style.display='none';
    document.getElementById("header").style.backgroundColor='black';
    document.body.scrollTop = 0;
    document.getElementById("body").style.height='100vh';
    document.getElementById("body").style.overflowY='hidden';
    document.getElementById("drop-down").style.overflowY='visible';
}

function drop_up(){
    document.getElementById("drop-down").style.height='0vh';
    document.getElementById("nav-cross-logo").style.display='none';
    document.getElementById("nav-logo").style.display='block';
    document.getElementById("header").style.backgroundColor='rgba(55,55,55,1)';
    document.getElementById("body").style.overflowY='visible';
}
